public class Boss {
    private int HealthOfBoss;
    private int DamageOfBoss;
    private String TypeOfDefense;

    public int getHealthOfBoss() {
        return HealthOfBoss;
    }

    public void setHealthOfBoss(int healthOfBoss) {
        HealthOfBoss = healthOfBoss;
    }

    public int getDamageOfBoss() {
        return DamageOfBoss;
    }

    public void setDamageOfBoss(int damageOfBoss) {
        DamageOfBoss = damageOfBoss;
    }

    public String getTypeOfDefense() {
        return TypeOfDefense;
    }

    public void setTypeOfDefense(String typeOfDefense) {
        TypeOfDefense = typeOfDefense;
    }
}
